package com.modofo.recordr;

public class RecorderEvent {
	public static final int RECORD_STOP = 1, PLAY_START=2,PLAY_STOP=3, RECORD_CANCEL=4;
	private int type;
	private byte[] bytes;

	public byte[] getBytes() {
		return bytes;
	}

	public void setBytes(byte[] bytes) {
		this.bytes = bytes;
	}

	public int getType() {
		return type;
	}

	public void setType(int type) {
		this.type = type;
	}
}
